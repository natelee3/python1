#Day of the Week
#Given a number 0-6, prints the corresponding day of the week


day = input("Day (0-6)? ")
if day == 0:
    print("Sunday")
elif day == 1:
    print("Monday")
elif day == 2:
    print("Tuesday")
elif day == 3:
    print("Wednesday")
elif day == 4:
    print("Thursday")
elif day == 5:
    print("Friday")
elif day == 6:
    print("Saturday")
else:
    print("That's not a number between 0 and 6. We'll try again later...")




